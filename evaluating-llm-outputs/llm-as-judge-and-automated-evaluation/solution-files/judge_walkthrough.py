"""Evaluating LLM Outputs 2 - LLM-as-Judge and Automated Evaluation.

Walkthrough of evidence-aware LLM-as-judge calibration. This driver:

1. Loads the judge calibration examples generated by build_eval_artifacts.py.
2. Scores each example with the deterministic heuristic_judge (so the lesson
   runs offline by default).
3. Scores each example with the live llm_judge when an OpenAI client is
   available, so the author can demonstrate the calibration delta.
4. Prints a side-by-side comparison plus a pass/partial/fail summary.

The judge implementation itself lives in tutorials/advanced-rag/_shared/judge.py
so both the Advanced RAG course (AR2) and the Evaluating LLM Outputs course
(EO2) call exactly the same code. When the EO course is authored, this
walkthrough is the lesson's reference; AR2 only ships the import.
"""

import argparse
import json
import os
import sys


SHARED = os.path.normpath(os.path.join(os.path.dirname(__file__), "..", "..", "..", "advanced-rag", "_shared"))
if SHARED not in sys.path:
    sys.path.insert(0, SHARED)

from judge import JudgeUnavailable, heuristic_judge, llm_judge


def read_jsonl(path):
    rows = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line:
                rows.append(json.loads(line))
    return rows


def load_corpus(path):
    corpus = {}
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            row = json.loads(line)
            corpus[row["chunk_id"]] = row
    return corpus


def default_rag_dir():
    if os.path.exists("/workspace/rag"):
        return "/workspace/rag"
    return "rag"


def evidence_payload(corpus, chunk_ids):
    payload = []
    for chunk_id in chunk_ids or []:
        if chunk_id in corpus:
            payload.append({
                "chunk_id": chunk_id,
                "title": corpus[chunk_id]["title"],
                "text": corpus[chunk_id]["text"],
            })
    return payload


def compare_scores(example, heuristic, live):
    """Compare an expected score, the heuristic score, and the live LLM score
    for one calibration example. Returns a small dict the lesson can table."""
    expected = example.get("expected_scores") or {}
    row = {"example_id": example["example_id"], "user_query": example["user_query"]}
    for dimension in ("faithfulness", "citation_correctness", "command_safety", "refusal_correctness"):
        row[dimension] = {
            "expected": expected.get(dimension),
            "heuristic": heuristic.get(dimension),
            "live": live.get(dimension) if live else None,
        }
    row["heuristic_rationale"] = heuristic.get("rationale")
    if live:
        row["live_rationale"] = live.get("rationale")
    return row


def get_openai_client():
    if not os.getenv("OPENAI_API_KEY"):
        return None
    try:
        from openai import OpenAI
    except ImportError:
        return None
    return OpenAI()


def main():
    parser = argparse.ArgumentParser(description="Walk through the evidence-aware LLM-as-judge.")
    parser.add_argument("--rag-dir", default=default_rag_dir())
    parser.add_argument("--use-llm", action="store_true",
                        help="If set and OPENAI_API_KEY is configured, also call the live LLM judge.")
    args = parser.parse_args()

    artifact_dir = os.path.join(args.rag_dir, "generated_eval_artifacts")
    corpus_path = os.path.join(args.rag_dir, "git_kb_corpus_full", "corpus.jsonl")

    corpus = load_corpus(corpus_path)
    examples = read_jsonl(os.path.join(artifact_dir, "judge_calibration_examples_draft.jsonl"))

    client = get_openai_client() if args.use_llm else None

    rows = []
    for example in examples:
        evidence = evidence_payload(corpus, example.get("evidence_chunk_ids"))
        heuristic = heuristic_judge(
            query=example["user_query"],
            answer=example["answer"],
            evidence=evidence,
            cited_ids=example.get("evidence_chunk_ids"),
            expected_behavior=example.get("expected_behavior"),
        )
        live = None
        if client is not None:
            try:
                live = llm_judge(
                    query=example["user_query"],
                    answer=example["answer"],
                    evidence=evidence,
                    client=client,
                    cited_ids=example.get("evidence_chunk_ids"),
                    expected_behavior=example.get("expected_behavior"),
                )
            except JudgeUnavailable:
                live = None
        rows.append(compare_scores(example, heuristic, live))

    print(json.dumps(rows, indent=2, sort_keys=True))


if __name__ == "__main__":
    main()
